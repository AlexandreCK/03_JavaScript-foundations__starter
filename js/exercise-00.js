/* 
   Create a constant variable of each of the following primitive types:
   - String
   - Number
   - Boolean
   - Null
   - Undefined

   Log out each variable to the terminal at the end.
*/

const stringVariable = "fil";
const numberVariable = 289.2;
const booleanVariable = true;
const nullVariable = null;

// for undefinded it can be done like this:
let undefinedVariable;
// or like this with const:
const undefinedConstVariable = undefined;

console.log("stringVariable: ", stringVariable);
console.log("numberVariable: ", numberVariable);
console.log("booleanVariable: ", booleanVariable);
console.log("nullVariable: ", nullVariable);
console.log("undefinedVariable: ", undefinedVariable);
console.log("undefinedConstVariable: ", undefinedConstVariable);
